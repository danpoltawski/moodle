class backup_outcomesupport_mod_plugin extends backup_outcome_area_plugin {
    public function define_module_plugin_structure() {
        $plugin  = $this->get_plugin_element();
        $wrapper = new backup_nested_element($this->get_recommended_name());

        $plugin->add_child($wrapper);

        $this->define_outcome_area_structure($wrapper, backup_helper::is_sqlparam('mod'), '../../../../../modulename',
            backup_helper::is_sqlparam('mod'), backup::VAR_PARENTID);
    }
}